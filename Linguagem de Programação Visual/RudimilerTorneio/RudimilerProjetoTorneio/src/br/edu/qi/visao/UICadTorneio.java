    private void cadastrar() {
        
        TorneioVO torneio = new TorneioVO();
        try {
            if(jtfNomeDoJogo.getText().isEmpty()) {
                throw new NumberFormatException("Nome inv√°lido!");
            }//fecha if(Nome do jogo)
            
            if(jtfNomeDoTorneio.getText().isEmpty()) {
                throw new NumberFormatException();
            }//fecha if(Nome do torneio)
            
            if(jtfTime.getText().isEmpty()) {
                throw new NumberFormatException();
            }//fecha if(Time)
            
            if(jtfTipoDeJogo.getText().isEmpty()) {
                throw new NumberFormatException();
            }//fecha if(Tipo de jogo)
            
            if(jtfParticipantes.getText().isEmpty()) {
                throw new NumberFormatException();
            }//fecha if(Participante)
            
            if(jtfRodadas.getText().isEmpty()) {
                throw new NumberFormatException();
            }//fecha if(Rodadas)
        } catch (Exception exception) {
            JOptionPane.showMessageDialog(rootPane,
                    "Erro!" + exception.getMessage());
        }//fecha catch
        
        
        torneio.setNomeDoJogo(jtfNomeDoJogo.getText());
        torneio.setNomeDoTorneio(jtfNomeDoTorneio.getText());
        torneio.setParticipantes(Integer.parseInt(jtfParticipantes.getText()));
        torneio.setRodadas(Integer.parseInt(jtfRodadas.getText()));
        torneio.setTimes(Integer.parseInt(jtfTime.getText()));
        torneio.setTipoDeJogo(jtfTipoDeJogo.getText());
    }//fecha cadastrar
    
    private void limpar() {
        
        jtfNomeDoJogo.setText("");
        jtfNomeDoTorneio.setText("");
        jtfParticipantes.setText("");
        jtfRodadas.setText("");
        jtfTime.setText("");
        jtfTipoDeJogo.setText("");
    }//fecha limpar
    
