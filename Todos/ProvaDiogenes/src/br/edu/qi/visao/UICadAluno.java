    private void cadastrar() {
        
        Aluno aluno = new Aluno();
        
        aluno.setNome(jtfNome.getText());
        aluno.setSobrenome(jtfSobrenome.getText());
        aluno.setNota1(Double.parseDouble(jtfNota1.getText()));
        aluno.setNota2(Double.parseDouble(jtfNota2.getText()));
        aluno.setPeso1(Double.parseDouble(jtfPeso1.getText()));
        aluno.setPeso2(Double.parseDouble(jtfPeso2.getText()));
        aluno.setPresencas(Integer.parseInt(jtfPresencas.getText()));
        aluno.setTotalAulas(Integer.parseInt(jtfTotalAulas.getText()));
        aluno.setSexo(bgSexo.getSelection().getActionCommand());
        
        jtaResultado.setText("Dados:\n " + aluno.toString());
        jtfNome.grabFocus();
    }//fecha cadastrar
    
    private void limpar() { 
        jtfNome.setText("");
        jtfSobrenome.setText("");
        jtfNota1.setText("");
        jtfNota2.setText("");
        jtfPeso1.setText("");
        jtfPeso2.setText("");
        jtfPresencas.setText("");
        jtfTotalAulas.setText("");
        bgSexo.clearSelection();
        jtfNome.grabFocus();
    }//fecha limpar
    
