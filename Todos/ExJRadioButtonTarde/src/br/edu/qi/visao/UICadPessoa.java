    private void cadastrar() {
        
        Pessoa pessoa  = new Pessoa();
        
        pessoa.setNome(jtfNome.getText());
        
        pessoa.setSexo(
                bgSexo.getSelection().getActionCommand());
        
//        JOptionPane.showMessageDialog(
//                rootPane,
//                "Dados: \n" + pessoa.toString(),
//                "Relat√≥rio",
//                JOptionPane.PLAIN_MESSAGE);

        jtaResposta.setText("Dados: " + pessoa.toString());
        
        
        
    }//fecha cadastrar
    
